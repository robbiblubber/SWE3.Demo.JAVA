package swe3.demo;



/** This exception is thrown when the requested object is already locked. */
public class ObjectLockedException extends Exception
{
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // protected members                                                                                                //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    
    /** Owner key. */
    protected String _ownerKey;
    
    
    
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // constructors                                                                                                     //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    
    /** Creates a new instance of this class.
     * @param ownerKey Owner key. */
    public ObjectLockedException(String ownerKey)
    {
        _ownerKey = ownerKey;
    }
    
    
    
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // public methods                                                                                                   //
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    
    /** Gets the owner key that has locked the object. */
    public String getOwnerKey()
    {
        return _ownerKey;
    }
}
